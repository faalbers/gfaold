//==============================================================================
#include "Point2D.h"
//==============================================================================
GFA::Point2D::Point2D()
    :   x(0.0),
        y(0.0)
{
}
//==============================================================================
GFA::Point2D::Point2D(const Scalar &x_, const Scalar &y_)
    :   x(x_),
        y(y_)
{
}
//==============================================================================
std::ostream & GFA::operator<< (std::ostream &os, GFA::Point2D const &rhs)
{
    os << "Point2D(" << rhs.x << ", " << rhs.y << ")";
    return os;
}
